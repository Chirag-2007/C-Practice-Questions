// Q7.  Add two numbers
// Problem statement
// Given two numbers, add them and print their sum
// Input
// Input contains two numbers between 1 and 100, separated by a space
// Output
// Print the sum of the 2 numbers
// Sample Input
// 1 2
// Sample Output
// 3

#include<stdio.h>
int main(){
    int n, m;
    scanf("%d %d",&n,&m);
    printf("%d",n + m);
    return 0;
}